
package com.trapper.projeto2;


public class EstruturaRepeticao {
    
    public static void main(String[] args) {
        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }
        
        System.out.println("-".repeat(10));
        
        Integer b = 0;
        
        while (b <= 10) {
            System.out.println(b);
            b++;
        }
    }
}
